int		count_tetr(char *str)
{
	unsigned int		i;
	unsigned int		tetr;

	i = 0;
	tetr = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '\n' && (str[i + 1] == '\n' || str[i + 1] == '\0'))
			tetr++;
		i++;
	}
	return (tetr);
}
