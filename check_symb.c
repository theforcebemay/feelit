#include "fillit.h"

int		check_symb(const char *str)
{
	unsigned int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '.' || str[i] == '#' || str[i] == '\n')
			i++;
		else
			return (1);
	}
	return (0);
}
